                                    <p style="text-align: justify; font-size:13.5px;  line-height:25px;">
                                        Dialysis is a process for removing waste and excess water from the blood and is used primarily as an artificial
                                        replacement for lost kidney function in people with kidney failure. Dialysis is used for those with an acute 
                                        disturbance in kidney function (acute kidney injury, previously acute renal failure) or progressive but chronically
                                        worsening kidney function. Dialysis may be used for very sick patients who have suddenly but temporarily, lost their
                                        kidney function (acute renal failure) or for quite stable patients who have permanently lost their kidney function. 
                                        Dialysis is an imperfect treatment
                                        to replace kidney function because it does not correct the endocrine functions of the kidney.
                                        
